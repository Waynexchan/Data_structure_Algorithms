class Solution(object):
    def singleNumber(self, nums):
        """
        :type nums: List[int]
        :rtype: int
        """
        result = 0
        for i in nums:
            result ^= i
        return result
        
nums = [2,2,1]
solution = Solution()
print(solution.singleNumber(nums))


# Time Complexity O(n)
# Space Complexity O (1)
        

